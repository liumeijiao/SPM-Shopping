package com.bupt.project1.utils;

public class Count {
    int cou;

    public int getCou() {
        return cou;
    }

    public void setCou(int cou) {
        this.cou = cou;
    }

    @Override
    public String toString() {
        return "Count{" +
                "cou=" + cou +
                '}';
    }
}
